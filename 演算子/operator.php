//3.1.1 文字列混在の演算
print '108' + '15';//結果 123
print '<br/>';
print '108' + '1.5XYZ';//結果 警告文 + 109.5
print '<br/>';
print '108' + 'XYZ10';//結果 警告文 + 108
print '<br/>';
print '108' + '1.5XYZ10';//結果 警告文 + 109.5
print '<br/>';
print '108' + '1.5E1';//結果 123
print '<br/>';
print '108' + '0b11';//結果 警告文 + 108
print '<br/>';
print '108' + '010';//結果 118
print '<br/>';
print '108' + '0x1A';//結果 警告文 + 108
